-- ============================================================================
-- Migration 045: Access Settings (merge of network + search_config)
-- ============================================================================
--
-- An Access Settings is the consumer's plan for one of their bots:
--   - a list of catalogues the bot is allowed to consume
--   - a max_price_eur ceiling that protects against publisher price hikes
--
-- An API key is now a (subscription, access_settings) pair. The bot identity
-- is carried by the access_settings (not the key) so multiple keys for the
-- same plan share the bot.
--
-- Sub-manager revenue (the historical 7% line) is rerouted via a new
-- `workspaces.referral_workspace_id` column. When NULL, the sub_manager
-- ledger row keeps `recipient_workspace_id = NULL` (absorbed by the platform
-- at reconciliation).
--
-- Catalogue eligibility for an access_settings:
--   - catalog.status = 'active'  (marketplace), or
--   - catalog.workspace_id = access_settings.workspace_id  (own private cat).
--
-- ⚠️ Destructive (MVP): api_keys is TRUNCATED. networks and network_catalogs
-- are dropped. Existing keys must be re-issued through the new flow.
-- credit_transactions.api_key_id is ON DELETE SET NULL so the ledger survives.
--
-- All statements are idempotent so a partial-applied remote can re-run this
-- without manual reconciliation.
-- ============================================================================

BEGIN;


-- ============================================================================
-- 1. workspaces.referral_workspace_id
-- ============================================================================

ALTER TABLE public.workspaces
  ADD COLUMN IF NOT EXISTS referral_workspace_id UUID NULL
    REFERENCES public.workspaces(id) ON DELETE SET NULL;

CREATE INDEX IF NOT EXISTS idx_workspaces_referral
  ON public.workspaces(referral_workspace_id)
  WHERE referral_workspace_id IS NOT NULL;

COMMENT ON COLUMN public.workspaces.referral_workspace_id IS
  'Sub-manager / reseller workspace that receives the sub_manager revenue '
  'share for every debit generated by THIS workspace''s subscriptions. NULL '
  'means there is no referral — the corresponding ledger row keeps '
  'recipient_workspace_id NULL and is absorbed by the platform.';


-- ============================================================================
-- 2. access_settings
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.access_settings (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  public_id       TEXT NOT NULL UNIQUE
                    CHECK (starts_with(public_id, 'as_')),
  workspace_id    UUID NOT NULL REFERENCES public.workspaces(id) ON DELETE CASCADE,
  bot_id          UUID NOT NULL REFERENCES public.bots(id)       ON DELETE RESTRICT,
  name            TEXT NOT NULL,
  max_price_eur   NUMERIC(10,4) NOT NULL
                    CHECK (max_price_eur >= 0),
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (workspace_id, name)
);

CREATE INDEX IF NOT EXISTS idx_access_settings_workspace ON public.access_settings(workspace_id);
CREATE INDEX IF NOT EXISTS idx_access_settings_bot       ON public.access_settings(bot_id);

COMMENT ON TABLE public.access_settings IS
  'Consumer plan: a named (bot, catalogues, max_price) tuple owned by a '
  'workspace. API keys reference one access_settings — its bot is the '
  'identity claimed at /licenses time, its catalogues are the allowlist, '
  'its max_price_eur caps publisher price hikes.';


-- ============================================================================
-- 3. access_settings_catalogs (junction, no invite lifecycle)
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.access_settings_catalogs (
  access_settings_id UUID NOT NULL REFERENCES public.access_settings(id) ON DELETE CASCADE,
  catalog_id         UUID NOT NULL REFERENCES public.catalogs(id)        ON DELETE CASCADE,
  added_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
  PRIMARY KEY (access_settings_id, catalog_id)
);

CREATE INDEX IF NOT EXISTS idx_access_settings_catalogs_catalog
  ON public.access_settings_catalogs(catalog_id);

COMMENT ON TABLE public.access_settings_catalogs IS
  'Catalogues bundled into an access_settings. Eligibility is enforced by a '
  'BEFORE INSERT trigger: catalog must either be on the marketplace '
  '(status=active) or belong to the same workspace as the access_settings.';


-- ============================================================================
-- 4. Trigger: bot must be in workspace_bots(workspace_id, bot_id)
-- ============================================================================

CREATE OR REPLACE FUNCTION public.validate_access_settings_bot()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM public.workspace_bots
    WHERE workspace_id = NEW.workspace_id
      AND bot_id       = NEW.bot_id
  ) THEN
    RAISE EXCEPTION
      'bot_not_in_workspace: workspace=% bot=%',
      NEW.workspace_id, NEW.bot_id
      USING ERRCODE = 'check_violation';
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_access_settings_validate_bot ON public.access_settings;
CREATE TRIGGER trg_access_settings_validate_bot
  BEFORE INSERT OR UPDATE OF workspace_id, bot_id ON public.access_settings
  FOR EACH ROW EXECUTE FUNCTION public.validate_access_settings_bot();


-- ============================================================================
-- 5. Trigger: catalog eligibility (marketplace OR own workspace)
-- ============================================================================

CREATE OR REPLACE FUNCTION public.validate_access_settings_catalog()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
  v_consumer_ws  UUID;
  v_catalog_ws   UUID;
  v_catalog_status TEXT;
BEGIN
  SELECT workspace_id INTO v_consumer_ws
  FROM public.access_settings
  WHERE id = NEW.access_settings_id;

  SELECT workspace_id, status INTO v_catalog_ws, v_catalog_status
  FROM public.catalogs
  WHERE id = NEW.catalog_id;

  IF v_catalog_ws IS NULL THEN
    RAISE EXCEPTION 'catalog_not_found: catalog=%', NEW.catalog_id
      USING ERRCODE = 'check_violation';
  END IF;

  IF v_catalog_status <> 'active' AND v_catalog_ws <> v_consumer_ws THEN
    RAISE EXCEPTION
      'catalog_not_eligible: catalog=% (status=%) workspace=% access_settings_workspace=%',
      NEW.catalog_id, v_catalog_status, v_catalog_ws, v_consumer_ws
      USING ERRCODE = 'check_violation';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_access_settings_catalogs_validate ON public.access_settings_catalogs;
CREATE TRIGGER trg_access_settings_catalogs_validate
  BEFORE INSERT ON public.access_settings_catalogs
  FOR EACH ROW EXECUTE FUNCTION public.validate_access_settings_catalog();


