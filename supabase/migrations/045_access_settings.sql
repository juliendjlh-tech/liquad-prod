  FOR ALL
  USING (
    access_settings_id IN (
      SELECT a.id FROM public.access_settings a
      JOIN public.workspace_members wm ON wm.workspace_id = a.workspace_id
      WHERE wm.user_id = auth.uid() AND wm.role IN ('owner', 'admin')
    )
  )
  WITH CHECK (
    access_settings_id IN (
      SELECT a.id FROM public.access_settings a
      JOIN public.workspace_members wm ON wm.workspace_id = a.workspace_id
      WHERE wm.user_id = auth.uid() AND wm.role IN ('owner', 'admin')
    )
  );


