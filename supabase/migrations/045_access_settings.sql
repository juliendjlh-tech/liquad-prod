  'catalogue allowlist + max_price cap consumed at /licenses time.';

CREATE OR REPLACE FUNCTION public.validate_api_key_bot_matches_access_settings()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
  v_expected_bot UUID;
BEGIN
  SELECT bot_id INTO v_expected_bot
  FROM public.access_settings
  WHERE id = NEW.access_settings_id;

  IF v_expected_bot IS NULL THEN
    RAISE EXCEPTION 'access_settings_not_found: access_settings=%',
      NEW.access_settings_id
      USING ERRCODE = 'check_violation';
  END IF;

  IF v_expected_bot <> NEW.bot_id THEN
    RAISE EXCEPTION
      'bot_mismatch: api_key.bot=% access_settings.bot=%',
      NEW.bot_id, v_expected_bot
      USING ERRCODE = 'check_violation';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_api_keys_validate_bot_matches_access_settings ON public.api_keys;
CREATE TRIGGER trg_api_keys_validate_bot_matches_access_settings
  BEFORE INSERT OR UPDATE OF bot_id, access_settings_id ON public.api_keys
  FOR EACH ROW EXECUTE FUNCTION public.validate_api_key_bot_matches_access_settings();


